def main() -> None:
    rows = []
    for month, lpr1, lpr5, bp_first, bp_second in LPR_HISTORY:
        m_first = round(lpr5 + bp_first / 100.0, 2)
        m_second = round(lpr5 + bp_second / 100.0, 2)
        rows.append(
            {
                "month": month,
                "lpr_1y": lpr1,
                "lpr_5y": lpr5,
                "mortgage_first": m_first,
                "mortgage_second": m_second,
                "source": "PBOC公开公告",
            }
        )

    OUT.parent.mkdir(parents=True, exist_ok=True)
    with OUT.open("w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(
            f,
            fieldnames=["month", "lpr_1y", "lpr_5y", "mortgage_first", "mortgage_second", "source"],
        )
        w.writeheader()
        w.writerows(rows)

    print(f"✅ {OUT} ({len(rows)} rows)")
    print(f"   range: {rows[0]['month']} → {rows[-1]['month']}")
    print(f"   1y {rows[0]['lpr_1y']} → {rows[-1]['lpr_1y']}")
    print(f"   5y {rows[0]['lpr_5y']} → {rows[-1]['lpr_5y']}")
    print(f"   首套 {rows[0]['mortgage_first']} → {rows[-1]['mortgage_first']}")
    print(f"   二套 {rows[0]['mortgage_second']} → {rows[-1]['mortgage_second']}")
    # 计算累计下调
    drop_5y = round(rows[0]['lpr_5y'] - rows[-1]['lpr_5y'], 2)
    drop_1y = round(rows[0]['lpr_1y'] - rows[-1]['lpr_1y'], 2)
    print(f"   5y 累计下调: -{drop_5y} pp, 1y 累计下调: -{drop_1y} pp")


if __name__ == "__main__":
    main()